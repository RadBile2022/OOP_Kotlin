package basicKotlin

fun main() {
    //operasi perbandingan
    val nilai1 = 70
    val nilai2 = 80

    val result1 = nilai1 > nilai2 //operator lebih dari
    val result2 = nilai1 < nilai2 //operator kurang dari
    println(result1)
    println(result2)

    val result3 = nilai1 >= nilai2 //perbandingan lebih dari sama dengan
    val result4 = nilai1 <= nilai2 //perbandingan kurang dari sama dengan
    println(result3)
    println(result4)


}